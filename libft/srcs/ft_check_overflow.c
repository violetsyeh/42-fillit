#include "libft.h"

int		ft_check_overflow(unsigned long nbr, int new_num, int sign)
{
	if (sign == 1)
		if ((nbr >= 922337203685477580 && new_num > 7)
			|| (nbr >= 922337203685477581))
			return (-1);
	if (sign == -1)
		if ((nbr >= 922337203685477580 && new_num > 8)
			| (nbr >= 922337203685477581))
			return (0);
	return (1);
}
