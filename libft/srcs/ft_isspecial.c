#include "libft.h"

int		ft_isspecial(int c)
{
	if (c >= '!' && c <= '/')
		return (1);
	else if (c >= ':' && c <= '@')
		return (1);
	else if (c >= '[' && c <= '`')
		return (1);
	else if (c >= '{' && c <= '~')
		return (1);
	return (0);
}
